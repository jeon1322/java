package day03;

public class Test04 {
	public static void main(String[] args) {
		int no1 = 5, no2 = 3;
		System.out.println("no1 / no2 : " + (no1 / no2));
		System.out.println("no1 + no2 : " + (no1 + no2));
		System.out.println("no1 - no2 : " + (no1 - no2));
		System.out.println("no1 * no2 : " + (no1 * no2));
		System.out.println("no1 % no2 : " + (no1 % no2));
		// 사칙연산자는 피연산자가 반드시 2개이상 필요하다.
		System.out.println("no1 % no2 : " + (no1 + no2 * no1));
	}
}
